# Storing the string in ordered manner using the respective index starting from 0
salutation = "hi there"
#01234567
print(salutation[0])
print(salutation[7])
# indexing using the start and the end
print(salutation[0:7])
# indexing using the stepover in a string
print(salutation[0:8:2])
print(salutation[::])
print(salutation[::-1])
print(salutation[-1])
print(salutation[-2])